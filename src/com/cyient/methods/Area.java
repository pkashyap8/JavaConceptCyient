package com.cyient.methods;

public class Area {

		

//Area of triangle
public  double areaOfTriangle(double base, double hight) {
		
		return (base*hight)/2;
	} 

	//Area of Square
public static double areaOfSquare(double side){
	 return side*side;
}

	//Area of Trapezium
public static double areaOfTrapezium(double base1, double base2, double hight) {
	return (base1+base2)*hight*0.5;
}
}


